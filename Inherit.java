import java.util.Stack;
public class Inherit {
    
    public static void main(String[] args) {
        Stack<Character> cStack  = new Stack<>();
        cStack.push('S');
        cStack.push('a');
        cStack.push('u');
        cStack.push('r');
        cStack.push('a');
        cStack.push('b');
        cStack.push('h');


        System.out.println(cStack.pop());
        System.out.println(cStack.pop());
        System.out.println(cStack.pop());
        System.out.println(cStack.pop());
        System.out.println(cStack.pop());
        System.out.println(cStack.pop());
        System.out.println(cStack.pop());




    }
}
